public class Barang {
    String nobarang;
    String nama;
    int harga;
    int qty;
    int totalbarang;

    public String getNobarang() {
        return nobarang;
    }

    public void setNobarang(String nobarang) {
        this.nobarang = nobarang;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public int getTotalbarang() {
        return totalbarang;
    }

    public void setTotalbarang(int totalbarang) {
        this.totalbarang = totalbarang;
    }    
}
